public class AssignmentOperator {
    public static void main(String[] args) {
        
        int A = 5;
        A += 10;      // A = A + 10
        System.out.println(A);
                
        int B = 15;
        B -= 10;      // B = B - 10
        System.out.println(B);
    
        int C = 5;
        C += 10;      // C = C * 10
        System.out.println(C);

        int D = 25;
        D /= 10;      // D = D / 10
        System.out.println(D);

        int E = 54;
        E %= 10;      // E = E % 10
        System.out.println(E);
    }
}
